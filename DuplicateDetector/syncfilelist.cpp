#include "syncfilelist.h"

//////////////////////////////////////////////////////////////////

DDSyncFileList::DDSyncFileList()
{
}

//////////////////////////////////////////////////////////////////

DDSyncFileList::~DDSyncFileList()
{
    QMutexLocker(&(this->lock));
    fileList.clear();
}

//////////////////////////////////////////////////////////////////

void DDSyncFileList::Append(const QFileInfoList& files)
{
    QMutexLocker(&(this->lock));
    fileList.append(files);
}

//////////////////////////////////////////////////////////////////

QFileInfo DDSyncFileList::TakeFirst()
{
    QMutexLocker(&(this->lock));
    return fileList.takeFirst();
}

//////////////////////////////////////////////////////////////////

bool DDSyncFileList::IsEmpty()
{
    QMutexLocker(&(this->lock));
    return fileList.isEmpty();
}

//////////////////////////////////////////////////////////////////

int DDSyncFileList::Count()
{
    QMutexLocker(&(this->lock));
    return fileList.count();
}

